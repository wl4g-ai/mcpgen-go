package mcptools

import (
	"context"
	"fmt"
	"github.com/mark3labs/mcp-go/mcp"
	"io"
	"jira-mcp-v10.7.4/internal/helpers"
	"time"
)

// Input Schema for the CurrentUser tool
const CurrentUserInputSchema = "{\n  \"type\": \"object\"\n}"

// Response Template for the CurrentUser tool (Status: 200, Content-Type: application/json)
const CurrentUserResponseTemplate_A = "# API Response Information\n\nBelow is the response template for this API endpoint.\n\nThe template shows a possible response, including its status code and content type, to help you understand and generate correct outputs.\n\n**Status Code:** 200\n\n**Content-Type:** application/json\n\n> Returned if the currently authenticated user is returned.\n\n## Response Structure\n\n- Structure (Type: object):\n"

// NewCurrentUserMCPTool creates the MCP Tool instance for CurrentUser
func NewCurrentUserMCPTool() mcp.Tool {
	return mcp.NewToolWithRawSchema(
		"CurrentUser",
		"Get current user session information - Returns information about the currently authenticated user's session. If the caller is not authenticated they will get a 401 Unauthorized status code.",
		[]byte(CurrentUserInputSchema),
	)
}

// CurrentUserHandler is the handler function for the CurrentUser tool.
// It reads tool arguments, forwards the request to the upstream service, and returns the response.
func CurrentUserHandler(ctx context.Context, request mcp.CallToolRequest) (*mcp.CallToolResult, error) {
	upstream := mcputils.GetUpstreamEndpoint()

	args := request.GetArguments()
	if args == nil {
		args = make(map[string]interface{})
	}
	contentType := ""
	startTime := time.Now()
	resp, err := mcputils.ForwardRequest(ctx, upstream, "GET", "/rest/auth/1/session", args, []string{}, contentType)
	if err != nil {
		return nil, fmt.Errorf("upstream request failed: %w", err)
	}
	defer resp.Body.Close()

	body, err := io.ReadAll(resp.Body)
	if err != nil {
		return nil, fmt.Errorf("failed to read upstream response: %w", err)
	}

	mcputils.LogResponse(ctx, resp.StatusCode, "GET", resp.Request.URL.String(), time.Since(startTime), body)

	if resp.StatusCode < 200 || resp.StatusCode >= 300 {
		return mcp.NewToolResultError(fmt.Sprintf("upstream error: status %d, body: %s", resp.StatusCode, string(body))), nil
	}

	if filePath, err := mcputils.SaveBinaryResponse(resp, body, "CurrentUser"); err != nil {
		return mcp.NewToolResultError(err.Error()), nil
	} else if filePath != "" {
		return mcp.NewToolResultText(fmt.Sprintf("Saved to: %s (%d bytes)", filePath, len(body))), nil
	}

	return mcp.NewToolResultText(string(body)), nil
}
